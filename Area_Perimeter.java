// Write a program to calculate Area and Perimeter of a Rectangle
public class Area_Perimeter
{
    public static void main (String args[])
    {
        double a, p, l, b;
        l = 7.0;
        b = 2.0;
        a = l*b;
        p = 2*l+b;
        System.out.println("Area = " + a + "cm sq");
        System.out.println("Perimeter = " + p + "cm");
    }
}