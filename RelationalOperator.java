//Relational Operators
public class RelationalOperator
{
    public static void main(String arg[])
    {
        System.out.println(5==3);//false
        System.out.println(5!=3);//true
        System.out.println(5>3);//true
        System.out.println(5<3);//false
        System.out.println(5>=3);//true
    }
}


