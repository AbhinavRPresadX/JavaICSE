// Write a program to calculate Speed 
//Speed(s)= Distance(d)/Time(t)
public class Speed
{
    public static void main(String arg[])
    {
        double s, d;
        int t;
        d=120;
        t=2;
        s=d/t;
        System.out.println("Distance = "+d+ "km");
        System.out.println("Time = "+t+ "hours");
        System.out.println("Speed = " + s + "km/h");
    }
}